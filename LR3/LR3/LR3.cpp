int randomchislo()                                 //получение случайного числа в диапозоне от -5 до 20
{
	int a = -5, b = 20;                            //определение границ массива
	return (rand() % (b - a + 1) + a);             //возвращение значения случайного числа в заданном диапозоне
}

void isclotrchisel()								//исключение из массива всех отрицательных чисел
{
	int* arr1;                                      //массив для заполнения
	int* arr2;                                      //вторичный массив, для заполнения положительными числами
	int razmer = 10;                                //размер массива
	int k = 0;                                      
	arr1 = new int[10];
	for (int i = 0; i < razmer; i++)                //заполнение массива рандомными числами
		arr1[i] = randomchislo();
	for (int i = 0; i < 10; i++)                    //вывод массива
		cout << arr1[i] << ' ';

	for (int i = 0; i < 10; i++)                    //подсчёт кол-ва положительных чисел
		if (arr1[i] > 0) k++;
	arr2 = new int[k];                              //создание массива для положительных чисел
	int k1 = 0;                                     //счётчик 
	for (int i = 0; i < razmer; i++)                //заполнение массива для положительных чисел
		if (arr1[i] > 0)
		{
			arr2[k1] = arr1[i];                     //заполнение вторичного массива положительными числами из первичного
			k1++;                                   //увеличение счётчика для полодительных чисел
		}
	delete[]arr1;                                   //удаление первичного массива
	cout << endl << endl;
	for (int i = 0; i < k; i++)                     //вывод массива с положительными числами
		cout << arr2[i] << ' ';
}

