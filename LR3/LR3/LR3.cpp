	liter = "UOIEAuoiea";                           //заполненый алфавит гласных

	for (int i = 0; i < s.length(); i++)            //перемещение по буквам строки 
		for (j = 0; j < liter.length(); j++)        //пермещение по буквам алфавита
			if (liter[j] == s[i])                   //поиск соответствия, если нашли записываем в строку для гласных
			{
				glasn += s[i];
			}

	cout << endl << "Строка, в которой будет производиться поиск гласных: " << s << endl;
